/*
  Operadores RELACIONAIS
    são utilizados para realizar comparações entre valores ou expressões, resultando sempre um valor boolean(TRUE ou FALSE)

    ( == ) - igual, resulta TRUE se as expressões forem iguais
    ( === ) - Idêntico, resulta TRUE se as expressões forem iguais e do mesmo tipo de dados.
    ( != ou <> ) - Diferente, resulta TRUE se as expressões forem diferentes.
    ( < ) - Menor
    ( > ) - Maior
    ( <= ) - Menor ou Igual
    ( >= ) - Maior ou Igual
*/