/*
  Operadores LÓGICOS
    são utilizados para combinar expressões lógicas entre si, agrupando testes condicionais.

    ( $a and $b ) - retorna TRUE se ambas forem verdadeiras
    ( $a or $b ) - retorna TRUE alguma das duas forem verdadeiras
    ( $a xor $b ) - VERDADEIRO se $a e $b foram verdadeiras, de forma exclusiva.
    ( ! $b ) - Negação, inverte o valor (true vira false) e (false vira true)
    ( $a && $b ) - retorna TRUE se ambas forem verdadeiras
    ( $a || $b ) - retorna TRUE alguma das duas forem verdadeiras    
*/